    private void boletoTabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_boletoTabMouseClicked
        int li = boletoTab.getSelectedRow();
        int col = boletoTab.getSelectedColumn();
        String texto = (String) boletoTab.getValueAt(li, col);
        String id = (String) boletoTab.getValueAt(li, 0);
        //idSelect = "";
        if(texto.equals("            X")){
            excluir(id);
        }else if(texto.equals("            O")){
            editar(id);
        }
        preencheTabel();
    }//GEN-LAST:event_boletoTabMouseClicked

