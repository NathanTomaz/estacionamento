    public ResultSet busca() {
        return bd.recupera("select * from tb_boletos");
    }
    
    public void limpar() {
        this.idSelect = "";
        this.aluguelTX.setText("");
        this.mesTX.setText("");
    }
    public void excluir(String id){
        bd.persistir("DELETE FROM tb_boletos where id = "+id);
        limpar();
    }
    public void editar(String id){
        ResultSet boletos;
        try{
        boletos =  bd.recupera("SELECT * FROM tb_boletos where id = "+id);
        while(boletos.next()){
            this.idSelect = boletos.getString("id");
            this.aluguelTX.setText(boletos.getString("aluguel"));
            this.mesTX.setText(boletos.getString("mes"));
        }
            
        }catch(Exception ex){
            System.out.println(ex.getMessage());
        }
    }
    
    
    public void preencheTabel() {
        try {
            ResultSet todos = bd.recupera("select * from tb_boletos");
            tBol.setNumRows(0);
            while (todos.next()) {
                String[] Nlinha = {
                    todos.getString("aluguel"),
                    todos.getString("mes"),
                    todos.getString("situacao"),
                    "            X", "            O"
                };
                tBol.addRow(Nlinha);
            }
        }catch(Exception ex){
            System.out.println(ex.getMessage());
        }
    }

