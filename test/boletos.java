    private void guardarBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarBtActionPerformed
        int confirma;
        confirma = JOptionPane.showConfirmDialog(null, "Deseja Gravar esses dados?");
        if (confirma == JOptionPane.YES_OPTION) {
            String alu, mes, sit = null;
            alu = aluguelTX.getText();
            mes = mesTX.getText();
            if (rPago.isSelected())
                sit = "Pago";
            else if (rPagar.isSelected())
                sit = "A pagar";
            else if (rVencido.isSelected())
                   sit = "Vencido";
           
            conectar aa = new conectar();
            try {
                if(idSelect.equals("")){
                    bd.persistir("INSERT INTO tb_boletos(aluguel, mes, situacao) "
                        + "VALUES('" + alu + "','" + mes + "','" + sit + "')");
                    JOptionPane.showMessageDialog(null, "Gravado com sucesso!");

                }else{
                    bd.persistir("update tb_boletos set "
                        + "aluguel = '" + alu + "'"
                        + ",mes = '" + mes + "'"
                        + ",sit = '" + sit + "' "
                        + "where id = '"+idSelect+"'");
                    JOptionPane.showMessageDialog(null, "Editado com sucesso!");
                }
                preencheTabel();

            } catch (Exception ex) {
                System.out.println(ex.getMessage()+" ERROR-BD");
            }

        }
        else {
            JOptionPane.showMessageDialog(null, "Cancelado!");
        }
    }//GEN-LAST:event_guardarBtActionPerformed

