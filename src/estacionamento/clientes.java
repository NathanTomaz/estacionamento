    public void editar(String id){
        ResultSet cliente;
        try{
        cliente =  bd.recupera("SELECT * FROM tb_clientes where id = "+id);
        while(cliente.next()){
            this.idSelect = cliente.getString("id");
            this.nomeTex.setText(cliente.getString("nome"));
            this.telText.setText(cliente.getString("telefone"));
            this.cpfTex.setText(cliente.getString("cpf"));
        }
            
        }catch(Exception ex){
            System.out.println(ex.getMessage());
        }
    }
    
    
    public void preencheTabel() {
        try {
            ResultSet todos = bd.recupera("select * from tb_clientes");
            tCli.setNumRows(0);
            while (todos.next()) {
                String[] Nlinha = {
                    todos.getString("id"),
                    todos.getString("nome"),
                    todos.getString("telefone"),
                    todos.getString("cpf"),
                    "            X", "            O"
                };
                tCli.addRow(Nlinha);
            }
        }catch(Exception ex){
            System.out.println(ex.getMessage());
        }
    }

    public ResultSet busca() {
        return bd.recupera("select * from tb_clientes");
    }
    
    public void limpar() {
        this.idSelect = "";
        this.nomeTex.setText("");
        this.telText.setText("");
        this.cpfTex.setText("");
    }


