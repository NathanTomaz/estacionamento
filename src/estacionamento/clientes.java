    private void clienteTabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_clienteTabMouseClicked
        int li = clienteTab.getSelectedRow();
        int col = clienteTab.getSelectedColumn();
        String texto = (String) clienteTab.getValueAt(li, col);
        String id = (String) clienteTab.getValueAt(li, 0);
        //idSelect = "";
        if(texto.equals("            X")){
            excluir(id);
        }else if(texto.equals("            O")){
            editar(id);
        }
        preencheTabel();
    }//GEN-LAST:event_clienteTabMouseClicked

    private void guardarBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarBtActionPerformed
        int confirma;
        confirma = JOptionPane.showConfirmDialog(null, "Deseja Gravar esses dados?");
        if (confirma == JOptionPane.YES_OPTION) {
            // verifica se o usuário clicou no botão YES
            String cli, tell, cpf;
            cli = nomeTex.getText();
            tell = telText.getText();
            cpf = cpfTex.getText();
            conectar bb = new conectar();

            try {

                if(idSelect.equals("")){
                    bd.persistir("INSERT INTO tb_clientes(nome, telefone, cpf) "
                        + "VALUES('" + cli + "','" + tell + "','" + cpf + "')");

                    JOptionPane.showMessageDialog(null, "Gravado com sucesso!");
                }else{
                    bd.persistir("update tb_clientes set "
                        + "nome = '" + cli + "'"
                        + ",telefone = '" + tell + "'"
                        + ",cpf = '" + cpf + "' "
                        + "where id = '"+idSelect+"'");
                    JOptionPane.showMessageDialog(null, "Editado com sucesso!");
                }
                preencheTabel();

            } catch (Exception ex) {
                System.out.println(ex.getMessage()+" ERROR");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Cancelado!");
        }

    }//GEN-LAST:event_guardarBtActionPerformed
